# Ejercicios difíciles de LeetCode

# 1. Resolver Ecuación
# Resuelve una ecuación representada como una cadena.

# 2. Máxima Ruta de un Triángulo
# Encuentra la suma máxima desde la parte superior de un triángulo hasta la base.

# 3. Sudoku Válido
# Verifica si un tablero de Sudoku es válido.

# 4. Dividir Arreglo en Subconjuntos Iguales
# Determina si puedes dividir un arreglo en dos subconjuntos con la misma suma.

# 5. Multiplicación de Cadenas
# Multiplica dos números grandes representados como cadenas.

# 6. Generar Números Telefónicos Válidos
# Genera todas las combinaciones válidas de números telefónicos.

# 7. Crear Diccionario de Prefijos
# Construye un diccionario basado en prefijos de palabras.

# 8. Encuentra el k-ésimo Elemento Más Grande en una Matriz Ordenada
# Encuentra el k-ésimo elemento más grande en una matriz ordenada.

# 9. Dijkstra para el Camino Más Corto
# Implementa el algoritmo de Dijkstra para encontrar el camino más corto.

# 10. Máximo Flujo
# Encuentra el flujo máximo entre dos nodos en un grafo.

# 11. Resolver Mazos
# Encuentra el camino más corto para resolver un laberinto.

# 12. Árbol Binario de Máxima Profundidad
# Calcula la máxima profundidad de un árbol binario.

# 13. Ordenamiento Topológico
# Realiza el ordenamiento topológico de un grafo dirigido.

# 14. Cadena de Edit Distance
# Encuentra la distancia mínima para convertir una cadena en otra.

# 15. Ruta Hamiltoniana
# Encuentra si existe una ruta Hamiltoniana en un grafo.

# 16. Compresión de Cadena
# Comprime una cadena según patrones repetidos.

# 17. Superposición de Rectángulos
# Determina si dos rectángulos se superponen.

# 18. Generar Árbol AVL
# Genera un árbol AVL balanceado desde una lista ordenada.

# 19. Reducir Número a 1
# Encuentra el número mínimo de operaciones para reducir un número a 1.

# 20. Algoritmo de Kruskal
# Encuentra el árbol de expansión mínima en un grafo.
