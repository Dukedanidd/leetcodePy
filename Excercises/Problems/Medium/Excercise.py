# Ejercicios intermedios de LeetCode

# 1. Subcadena más Larga sin Repetir Caracteres
# Encuentra la longitud de la subcadena más larga que no tenga caracteres repetidos.

# 2. Productos Excepto el Propio
# Calcula un arreglo donde cada elemento sea el producto de todos los números excepto el propio.

# 3. Rotar Matriz 90 Grados
# Rota una matriz cuadrada 90 grados hacia la derecha.

# 4. Suma de Submatriz Máxima
# Encuentra la suma máxima de cualquier submatriz en una matriz 2D.

# 5. Número que Aparece una Vez
# En un arreglo donde todos los elementos se repiten excepto uno, encuentra el único elemento único.

# 6. Sumar Listas Enlazadas
# Dados dos números representados como listas enlazadas, suma los números y devuelve el resultado en una lista enlazada.

# 7. Cadena Zigzag
# Convierte una cadena a un formato zigzag y la devuelve como una nueva cadena.

# 8. Secuencia Más Larga Consecutiva
# Encuentra la longitud de la secuencia consecutiva más larga en un arreglo.

# 9. Buscar en Matriz Ordenada
# Determina si un número está presente en una matriz ordenada.

# 10. Permutaciones de una Cadena
# Genera todas las permutaciones posibles de una cadena dada.

# 11. Combinar Intervalos
# Dado un arreglo de intervalos, combina todos los intervalos que se solapan.

# 12. Validar Paréntesis
# Verifica si una cadena de paréntesis, corchetes y llaves es válida.

# 13. Reconstruir Orden Original
# Reconstruye el orden original de personas basado en su altura y posición.

# 14. Buscar el k-ésimo Número Mayor
# Encuentra el k-ésimo número más grande en un arreglo.

# 15. Cálculo de Potencias
# Implementa una función para calcular potencias sin usar el operador `**`.

# 16. Triángulo de Pascal
# Genera las primeras n filas del triángulo de Pascal.

# 17. Intersección de Dos Arreglos
# Devuelve los elementos comunes entre dos arreglos.

# 18. Número de Islas
# Cuenta cuántas islas (áreas conectadas de 1s) hay en una matriz binaria.

# 19. Transformación de Palabras
# Encuentra la longitud mínima de transformación para convertir una palabra en otra.

# 20. Subarreglos que Suman un Objetivo
# Encuentra cuántos subarreglos tienen una suma específica.
